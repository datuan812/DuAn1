    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblSanPham.getModel();
        dtm.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            SanPhamCustom el = list.get(i);
            Object[] rowData = {
                dtm.getRowCount() + 1,
                el.getMa(),
                el.getTen(),
                el.getMau(),
                el.getSize(),
                el.getSoLuongTon(),
                el.getNamBaoHanh(),
                el.getDonVi(),
                el.getGiaNhap() + "VNĐ"
            };
            dtm.addRow(rowData);
        }
    }

