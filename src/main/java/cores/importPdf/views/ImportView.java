    public SanPhamCustom getFormData() {
        if (txtMaSP.getText().trim().length() == 0) {
            MsgBox.alert(this, "Bạn phải nhập mã SP");
            return null;
        } else if (txtTenSP.getText().trim().length() == 0) {
            MsgBox.alert(this, "Bạn phải nhập tên sản phẩm");
            return null;
        }
        if (txtSoLuongTon.getText().trim().length() == 0) {
            MsgBox.alert(this, "Bạn phải nhập số lượng sản phẩm");
            return null;
        }
        int soLuongTon = 0;
        try {
            soLuongTon = Integer.parseInt(txtSoLuongTon.getText());
            if (soLuongTon < 0) {
                JOptionPane.showMessageDialog(this, "Bạn không được nhập số lượng nhỏ hơn 0");
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Bạn phải nhập là kiểu số");
            e.printStackTrace();
            return null;
        }
        if (txtDonVi.getText().trim().length() == 0) {
            MsgBox.alert(this, "Bạn phải nhập đơn vị");
            return null;
        }
        if (txtNamBH.getText().trim().length() == 0) {
            MsgBox.alert(this, "Bạn phải nhập năm bảo hành");
            return null;
        }
        int namBH = 0;
        try {
            namBH = Integer.parseInt(txtNamBH.getText());
            if (namBH < 0) {
                JOptionPane.showMessageDialog(this, "Bạn không được nhập năm bảo hành nhỏ hơn 0");
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Bạn phải nhập là kiểu số");
            e.printStackTrace();
            return null;
        }
        if (cbbMauSac.getSelectedItem().equals(" ")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn màu sắc");
            return null;
        }

        if (txtSize.getText().trim().length() == 0) {
            MsgBox.alert(this, "Bạn phải nhập size sản phẩm");
            return null;
        }
        int SizeSo = 0;
        try {
            SizeSo = Integer.parseInt(txtSize.getText());
            if (SizeSo < 0) {
                JOptionPane.showMessageDialog(this, "Bạn không được nhập size nhỏ hơn 0");
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Bạn phải nhập là kiểu số");
            e.printStackTrace();
            return null;
        }

        if (txtGiaNhap.getText().trim().length() == 0) {
            MsgBox.alert(this, "Bạn phải nhập giá nhập");
            return null;
        }

        BigDecimal giaNhap = null;
        try {
            giaNhap = new BigDecimal(txtGiaNhap.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Bạn phải nhập là kiểu số");
            e.printStackTrace();
            return null;
        }
        SanPhamCustom spct = new SanPhamCustom();
        spct.setDonVi(txtDonVi.getText());
        spct.setGiaNhap(giaNhap);
        spct.setMa(txtMaSP.getText());
        spct.setMau(cbbMauSac.getSelectedItem().toString());
        spct.setNamBaoHanh(namBH);
        spct.setSize(SizeSo);
        spct.setSoLuongTon(soLuongTon);
        spct.setStt("");
        spct.setTen(txtTenSP.getText());
        return spct;
    }

    public void clearForm() {
        txtDonVi.setText("");
        txtGiaNhap.setText("");
        txtMaSP.setText("");
        txtNamBH.setText("");
        txtSize.setText("");
        txtSoLuongTon.setText("");
        txtTenSP.setText("");
        cbbMauSac.setSelectedIndex(0);
    }

